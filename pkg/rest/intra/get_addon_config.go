package intra

import (
	"net/http"

	"github.com/gin-gonic/gin"
	"github.com/zhiqiangxu/ont-gateway/pkg/io"
	"github.com/zhiqiangxu/ont-gateway/pkg/service"
)

// GetAddonConfig for fetch a addon config
func GetAddonConfig(c *gin.Context) {
	var input io.GetAddonConfigInput
	if err := c.ShouldBind(&input); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"message": err.Error()})
		return
	}

	output := service.Instance().GetAddonConfig(input)
	sendoutput(c, output.Code, output)
}
